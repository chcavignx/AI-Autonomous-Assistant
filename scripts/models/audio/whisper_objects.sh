)

# Loop over each model
for name in "${!MODELS[@]}"; do
    url="${MODELS[$name]}"
    # Use -c to resume the download if interrupted
    echo "Downloading $name from $url..."
    wget -c "$url" -O "$TARGET_DIR/$name.pt"
done

for object in "${GPT2[@]}"; do
    echo "Downloading of : $object"
    wget -c "$object" -P "$TARGET_DIR"
done

